package com.primovision.lutransport.core.util;

public class MimeUtil {
    public static String getContentType(String ext) {
	if ("jpg".equalsIgnoreCase(ext) || "jpeg".equalsIgnoreCase(ext)) {
	    return "image/jpg";
	}
	if ("gif".equalsIgnoreCase(ext)) {
	    return "image/gif";
	}
	if ("pdf".equalsIgnoreCase(ext)) {
	    return "application/pdf";
	}
	if ("ppt".equalsIgnoreCase(ext) || "pptx".equalsIgnoreCase(ext)) {
	    return "application/vnd.ms-powerpoint";
	}
	if ("doc".equalsIgnoreCase(ext) || "docx".equalsIgnoreCase(ext)) {
	    return "application/vnd.ms-word";
	}
	if ("xls".equalsIgnoreCase(ext) || "xlsx".equalsIgnoreCase(ext)) {
	    return "application/vnd.ms-excel";
	}
	if ("tiff".equalsIgnoreCase(ext) || "tif".equalsIgnoreCase(ext)) {
	    return "image/tiff";
	}
	if ("flv".equalsIgnoreCase(ext)) {
	    return "video/x-flv";
	}
	if ("csv".equalsIgnoreCase(ext)) {
	    return "text/csv";
	}
	if ("html".equalsIgnoreCase(ext) || "print".equalsIgnoreCase(ext)) {
	    return "text/html";
	}
	if ("rtf".equalsIgnoreCase(ext)) {
	    return "application/rtf";
	}
	if ("txt".equalsIgnoreCase(ext)) {
	    return "text/plain";
	}
	if ("zip".equalsIgnoreCase(ext) || "jpeg".equalsIgnoreCase(ext)) {
	    return "application/zip";
	}
	return "application/x-octet-stream";
    }
}