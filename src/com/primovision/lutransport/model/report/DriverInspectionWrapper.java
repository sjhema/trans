package com.primovision.lutransport.model.report;

public class DriverInspectionWrapper {

	
	private String company;
	private String terminal;
	private String driver;
	private String enteredBy;
	private String weekOfDate;
	private String weekDateDay;
	private String inspectionStatus;
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getTerminal() {
		return terminal;
	}
	public void setTerminal(String terminal) {
		this.terminal = terminal;
	}
	public String getDriver() {
		return driver;
	}
	public void setDriver(String driver) {
		this.driver = driver;
	}
	public String getEnteredBy() {
		return enteredBy;
	}
	public void setEnteredBy(String enteredBy) {
		this.enteredBy = enteredBy;
	}
	public String getWeekOfDate() {
		return weekOfDate;
	}
	public void setWeekOfDate(String weekOfDate) {
		this.weekOfDate = weekOfDate;
	}
	public String getWeekDateDay() {
		return weekDateDay;
	}
	public void setWeekDateDay(String weekDateDay) {
		this.weekDateDay = weekDateDay;
	}
	public String getInspectionStatus() {
		return inspectionStatus;
	}
	public void setInspectionStatus(String inspectionStatus) {
		this.inspectionStatus = inspectionStatus;
	}
	
	
	
	
}


